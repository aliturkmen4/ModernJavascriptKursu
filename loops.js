//--------------------------------------------------------------------------------------------------------------------------

//for loop

for(let i=0;i<10;i++)
{
    if(i==3)
    {
        console.log("En sevdiğim rakam: "+i);
        continue; //döngünün kaldığı yerden devam etmesi için 5.satıra gönderir! yani 3 değerini ekrana yazdırmam!
    }

    if(i==6)
    {
        console.log("En sevmediğim rakam: "+i);
        break; //break gördüğü anda döngü sonlanır!!!
    }

    console.log(i);
}

//--------------------------------------------------------------------------------------------------------------------------

//while loop

let j=0;

while(j<10)
{
    console.log(j);
    j++;
}

